#!/usr/bin/env python3
"""
------
这是一个打印不同分割线你的实力模块
"""
def starbar(num):
    """
    打印 * 分割线
    :arg num: 线长
    """
    print('*' * num)

def hashbar(num):
    """
    打印#分割线
    :arg num: 线长
    """
    print("#" * num)

def simplebar(num):
    """
    打印 - 分割线
    :arg num: 线长
    """
    print("-" * num)
